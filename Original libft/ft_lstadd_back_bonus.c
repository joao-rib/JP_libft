#include "libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*ultima;

	if (lst && new)
	{
		if (*lst)
		{
			ultima = ft_lstlast(*lst);
			ultima->next = new;
		}
		else
			*lst = new;
	}
}
